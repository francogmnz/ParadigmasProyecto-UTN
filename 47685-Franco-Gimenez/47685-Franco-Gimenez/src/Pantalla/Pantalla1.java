    private void botonMostrarAtributoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonMostrarAtributoActionPerformed
        textoAreaImpresion.setText("");
        int index = listaRegistros.getSelectedIndex();
        String linea = " ";
        switch(index){
            case 0:
                linea += "MARCA\n  " + TextoMarca.getText();
                textoAreaImpresion.append(linea);
                break;
            case 1:
                linea += "PESO\n  " + textoPesoCoche.getText()+"kg";
                textoAreaImpresion.append(linea);
                break;
            case 2: 
                linea += "MOTOR\n  Cilindrada  " + textoCilindradaMotor.getText()+"\n  Cilindros  " + textoCilindrosMotor.getText()
                        + "\n  Numero  " + textoNroMotor.getText()+"\n  Precio  " + textoPrecioMotor.getText() +
                         "\nCARROCERÃA\n  Numero  " + textoNroCarroceria.getText() + "\n  Color  " + textoColorCarroceria.getText() +
                        "\n  Descripcion  " + textoDescripcionCarroceria.getText() + "\n  Precio  " + textoPrecioCarroceria.getText();
                textoAreaImpresion.append(linea);
                break;
            default:
                textoAreaImpresion.append("NO SE HA SELECCIONADO NINGUN ELEMENTO, POR FAVOR SELECCIONE UNO");
        }
    }//GEN-LAST:event_botonMostrarAtributoActionPerformed

    private void botonCargarCocheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCargarCocheActionPerformed
        // TODO add your handling code here:
        
        String marca = (String)TextoMarca.getText(); 
        int peso = Integer.parseInt(textoPesoCoche.getText());
        int cantidadCilindros = Integer.parseInt(textoCilindrosMotor.getText());
        int cilindrada = Integer.parseInt(textoCilindradaMotor.getText());
        Integer documentoMotor = Integer.parseInt(textoNroMotor.getText());
        float precioMotor = Float.parseFloat(textoPrecioMotor.getText());
        String color = textoColorCarroceria.getText(); 
        String descripcion = textoDescripcionCarroceria.getText();
        Integer documentoCarroceria = Integer.parseInt(textoNroCarroceria.getText());
        float precioCarroceria =  Float.parseFloat(textoPrecioCarroceria.getText());
        
        control.grabarCoche(peso, cantidadCilindros, cilindrada, documentoMotor, precioMotor, color, descripcion, 
                documentoCarroceria, precioCarroceria);
        
        elementosLista.removeAllElements();
        botonCargarCoche.setEnabled(false);
        botonInventario.setEnabled(false);
        botonComponente.setEnabled(false);
        botonCoche.setEnabled(true);
        botonCoche.setSelected(true);
        botonCrearInventario.setVisible(false);
        botonCrearCoche.setVisible(true);
        botonCrearCoche.setEnabled(true);
        botonMostrarAtributo.setEnabled(true);
        botonMostrarInventario.setEnabled(true);
        botonGrabarInventario.setEnabled(true);
        TextoMarca.setEditable(false);
        panelCoche.setVisible(true);
        panelComponentes.setVisible(false);
        String unItem = "Inventario";
        elementosLista.addElement(unItem);
    }//GEN-LAST:event_botonCargarCocheActionPerformed

